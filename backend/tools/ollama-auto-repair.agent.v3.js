const ROOT = process.cwd();

/* =========================
   PROJECT SCAN
========================= */
export function scanProject(dir = "backend") {
  const results = [];

  function walk(folder) {
    const files = fs.readdirSync(folder);

    for (const file of files) {
      const fullPath = path.join(folder, file);

      if (fs.statSync(fullPath).isDirectory()) {
        walk(fullPath);
      } else if (file.endsWith(".js")) {
        const content = fs.readFileSync(fullPath, "utf-8");

        results.push({
          file: fullPath,
          analysis: analyze(content),
        });
      }
    }
  }

  walk(dir);
  return results;
}

/* =========================
   CODE ANALYSIS ENGINE
========================= */
function analyze(content) {
  return {
    imports: extractImports(content),
    exports: extractExports(content),
    issues: detectIssues(content),
  };
}

/* =========================
   IMPORTS
========================= */
function extractImports(content) {
  const regex = /import\s+(?:{[^}]*}|\w+)\s+from\s+["'](.+?)["']/g;
  const imports = [];
  let m;

  while ((m = regex.exec(content)) !== null) {
    imports.push(m[1]);
  }

  return imports;
}

/* =========================
   EXPORTS
========================= */
function extractExports(content) {
  const exports = [];

  const named =
    content.match(/export\s+(const|function|class)\s+(\w+)/g) || [];

  named.forEach((e) => exports.push(e.split(" ").pop()));

  if (content.includes("export default")) {
    exports.push("default");
  }

  return exports;
}

/* =========================
   ISSUE DETECTION
========================= */
function detectIssues(content) {
  const issues = [];

  if (content.includes("require(") && content.includes("import")) {
    issues.push("MIXED_MODULE_SYSTEM");
  }

  if (content.includes("undefined")) {
    issues.push("POTENTIAL_RUNTIME_ERROR");
  }

  if (content.includes("..//")) {
    issues.push("INVALID_PATH_SYNTAX");
  }

  return issues;
}

/* =========================
   BROKEN IMPORT CHECK
========================= */
function checkBrokenImports(imports, filePath) {
  return imports.filter((imp) => {
    const resolved = path.resolve(path.dirname(filePath), imp);

    return !(
      fs.existsSync(resolved + ".js") ||
      fs.existsSync(resolved)
    );
  });
}

/* =========================
   AI REPAIR PLANNER
========================= */
function generateRepairPlan(file, analysis) {
  const fixes = [];

  const broken = checkBrokenImports(
    analysis.imports,
    file
  );

  for (const b of broken) {
    fixes.push({
      type: "CREATE_MODULE",
      target: b,
      priority: "HIGH",
    });
  }

  for (const issue of analysis.issues) {
    fixes.push({
      type: "CODE_ISSUE",
      issue,
      priority: "MEDIUM",
    });
  }

  return fixes;
}

/* =========================
   AUTO FIX EXECUTOR
========================= */
function applyFix(fix) {
  if (fix.type === "CREATE_MODULE") {
    createStub(fix.target);
  }
}

/* =========================
   STUB GENERATOR
========================= */
function createStub(modulePath) {
  const fullPath = path.resolve(ROOT, modulePath + ".js");

  if (fs.existsSync(fullPath)) return;

  fs.mkdirSync(path.dirname(fullPath), { recursive: true });

  fs.writeFileSync(
    fullPath,
    `// AUTO-GENERATED BY OLLAMA V3 SELF-HEALING ENGINE\n\nexport default function ${path.basename(
      modulePath
    )}() {\n  return "Stub module: ${modulePath}";\n}\n`
  );
}

/* =========================
   SELF HEALING LOOP ENGINE
========================= */
export async function runSelfHealing(iterations = 3) {
  console.log("🤖 OLLAMA V3 SELF-HEALING STARTED");

  for (let i = 0; i < iterations; i++) {
    console.log(`🔁 ITERATION ${i + 1}`);

    const scan = scanProject("backend");

    let totalFixes = 0;

    for (const file of scan) {
      const plan = generateRepairPlan(
        file.file,
        file.analysis
      );

      for (const fix of plan) {
        applyFix(fix);
        totalFixes++;
      }
    }

    console.log(`🛠 FIXES APPLIED: ${totalFixes}`);

    if (totalFixes === 0) {
      console.log("✅ SYSTEM STABLE - NO MORE ERRORS");
      break;
    }
  }

  console.log("🚀 SELF-HEALING COMPLETE");
}

